package reference;

public class Subject {
	String subjectName;
	int score;

	public void setName(String name) {
		subjectName = name;
	}
	
	public int getScore() {
		return score;
	}
	
	public void setScore(int score) {
		this.score = score;
	}
	
	public String SubjectName() {
		return subjectName;
	}
	
}
